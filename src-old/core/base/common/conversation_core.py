from typing import List

from src.core.base.common.base_core import BaseCore
from src.classes.base_agent.models import ConversationMessage, MessageRole


class ConversationCore(BaseCore):
    """
    Authoritative engine for conversation state.
    """

    def __init__(self, max_messages: int = 100) -> None:
        """Initializes the ConversationCore with an optional max_messages limit."""
        super().__init__()
        self.messages: List[ConversationMessage] = []
        self.max_messages = max_messages

    def add_message(self, role: MessageRole, content: str) -> None:
        """Add a new message to the conversation history."""
        msg = ConversationMessage(role=role, content=content)
        self.messages.append(msg)
        if len(self.messages) > self.max_messages:
            self.messages = self.messages[-self.max_messages :]

    def get_history(self) -> List[ConversationMessage]:
        """Return a copy of the conversation history."""
        return self.messages.copy()

    def clear(self) -> None:
        """Clear the conversation history."""
        self.messages.clear()
